package com.qa.javacodes.inheritance;

public class Parent {
	
	public void  startP() {
		System.out.println("start in Parent");
	}
	
	public void  stopP() {
		System.out.println("stop in Parent");
	}
	
	public void extraP() {
		System.out.println("extra in Parent");
	}
	
	//static
	
	public static void P() {
		System.out.println("static in Parent");
	}

}
